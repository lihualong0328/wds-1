@ ******************************************************************************
@ File：led_on.S
@ 功能：LED点灯程序，点亮LED1
@ ******************************************************************************       
            
.text
.global _start
_start:     
            LDR     R0,=0x56000050      @ R0设为GPFCON寄存器, 此寄存器用于选择端口B各引脚的功能: 是输出/输入/其他
            MOV     R1,#0x00000100      @ gpf4[9:8]=0b01  
            STR     R1,[R0]             @ 设置GPF4为输出口, 位[9:8]=0b01

            LDR     R0,=0x56000054      @ R0设为GPBDAT寄存器, 此寄存器用于读/写端口B各引脚的数据
            MOV     R1,#0x00000000      @ 此值改为0x00000010, 可让LED1熄灭
            STR     R1,[R0]             @ GPF4输出0, LED1点亮
MAIN_LOOP:
            B       MAIN_LOOP
